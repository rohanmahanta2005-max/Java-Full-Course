package com.rohan.basic;

class Pattern {
    public static void main(String[] args){
        System.out.println("         *");
        System.out.println("       * *");
        System.out.println("     * * *");
        System.out.println("   * * * *");
        System.out.println(" * * * * *");
    }

    public static class RightHalfPyramid {
        public static void main(String[] args){
            System.out.println("* * * * *");
            System.out.println("* * * *");
            System.out.println("* * *");
            System.out.println("* *");
            System.out.println("*");
        }
    }
}
